public class scribbleObject {

    public int page;
    public int stroke;
    public float X1;
    public float X2;
    public float Y1;
    public float Y2;

    public scribbleObject(int page, int stroke, float X1, float X2, float Y1,float Y2){
        this.page = page;
        this.stroke = stroke;
        this.X1 = X1;
        this.X2 = X2;
        this.Y1 = Y1;
        this.Y2 = Y2;

    }
}
